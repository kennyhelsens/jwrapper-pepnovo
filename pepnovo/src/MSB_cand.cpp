	// consider replacing with Z
	for (i=start_idx; i<end_idx; i++)
	{
		if (current_seq.marked_aa[i] || 
			(current_seq.seq[i] != Lys && current_seq.seq[i] != Gln) )
			continue;


		MSB_alternates new_alt;
		MSB_seq new_seq;
		int new_aa = Z_SYM;
			
		new_seq.clone_and_replace(current_seq,i,1,&new_aa,1);
		new_seq.marked_aa[i]=1;
		new_alt.alt_seqs.push_back(new_seq);
		new_alt.calaculate_scores(current_seq,config);

		add_mod_seqs(new_alt);
	}

	if (oxidized_met_aa>0)
	{
		int i;

		for (i=0; i<current_seq.seq_length; i++)
		{
			if (current_seq.marked_aa[i] || 
				(current_seq.seq[i] != Phe && current_seq.seq[i] != oxidized_met_aa) )
				continue;

			int aa_to_add = (current_seq.seq[i] == Phe) ? oxidized_met_aa : Phe;

			MSB_alternates new_alt;
			MSB_seq new_seq;
				
			new_seq.clone_and_replace(current_seq,i,1,&aa_to_add,1);
			new_seq.marked_aa[i]=1;
			new_alt.alt_seqs.push_back(new_seq);
			new_alt.alt_seqs.push_back(current_seq);
			new_alt.alt_seqs[1].marked_aa[i]=1;
			new_alt.calaculate_scores(current_seq,config);

			add_mod_seqs(new_alt);
		}
	}

//	if (this->candidate_mods.size()>0)

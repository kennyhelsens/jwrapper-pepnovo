/*****************************************************
// replace look for better paths
// looks for bad portions (2-4 amino acids long). Tries
// alternative paths.
******************************************************/
void MSB_candidate:: generate_alt_sub_path(PrmGraph *prm, BasicSpectrum *spec, 
										   ScoreModel *model,float pm_with_19)
{
	const float min_prob_improvement = 0.1;
	Config *config = prm->get_config();
	const int c_term_node = prm->get_num_nodes()-1;

	int i;

	for (i=0; i<current_seq.seq_length; i++)
	{
		int j,k;
		if (current_seq.aa_probs[i]>0.9) // don't start with good amino acid
				continue;

		// don't start in the middle of a double edge
		if (i>0 && current_seq.breakage_idxs[i] == current_seq.breakage_idxs[i-1])
			continue;

		for (j=2; j<=4; j++)
		{
			if (i>current_seq.seq_length-j)
				continue;

			// don't finish in the middle of a double edge
			if ( current_seq.breakage_idxs[i+j] == current_seq.breakage_idxs[i+j-1])
				continue;


			bool good_path = true;
			float prob = 1.0;
			for (k=0; k<j; k++)
			{
				const int idx = i+k;
				if (current_seq.seq[idx] > 100 || current_seq.aa_probs[idx]<=0)
				{
					good_path=false;
					break;
				}
				prob *= current_seq.aa_probs[idx];
			}

			if (! good_path)
				continue;

			prob = pow(prob,1.0/j);

			if (prob>0.7)  // don't bother if prob is high
				continue;

			// the prob of the aa on the right end must also be high
			if (current_seq.aa_probs[i+j]<0.6)
				continue;
			
			int n_idx = current_seq.breakage_idxs[i];
			int c_idx = current_seq.breakage_idxs[i+j];

			if (n_idx<0 || c_idx<0) // can't work with invalid breakage idxs
				continue;

			int n_aa = ( i == 0) ? Gap: current_seq.seq[i-1];
			int c_aa = ( i+j == current_seq.seq_length) ? Gap : current_seq.seq[i+j];

			vector<DeNovoPath> alt_subs;
		
			prm->get_alternate_sub_paths(spec,model,pm_with_19,n_idx,n_aa,c_idx,c_aa,alt_subs);
			

			float min_alt_prob = prob + min_prob_improvement;

			int count=0;
			for (k=0; k<alt_subs.size() && count<4; k++)
			{
				if (alt_subs[k].seq_prob<min_alt_prob)
					break;

				if (are_ILQK_equivalent(current_seq.seq+i,j,alt_subs[k].seq,alt_subs[k].seq_length))
					continue;

				if (i+j == current_seq.seq_length && current_seq.seq[i+j-1] == Lys &&
					(alt_subs[k].seq[j-1] == Gln || alt_subs[k].seq[j-1] == Glu) )
					continue;

				if (alt_subs[k].c_node_idx)
				{
					const int last_aa = alt_subs[k].seq[alt_subs[k].seq_length-1];
					if (last_aa == Glu || last_aa == Gln)
						continue;
				}

				// generate new cand	
				//	alt_subs[k].print(config);
		
				MSB_alternates new_alt;
				MSB_seq new_seq;
				new_seq.clone_and_swap_in(current_seq,i,j,alt_subs[k]);

				// check to see that this seq has not been added already
				int k;
				bool dont_add=false;
				for (k=0; k<candidate_mods.size(); k++)
				{
					int m;
					bool found_same=false;

					for (m=0; m<candidate_mods[k].alt_seqs.size(); m++)
						if (candidate_mods[k].alt_seqs[m] == new_seq)
						{
							found_same=true;
							break;
						}
					if (found_same)
					{
						dont_add=true;
						break;
					}
				}	
				if (dont_add)
					continue;

				new_alt.alt_seqs.push_back(new_seq);
				new_alt.alt_seqs.push_back(current_seq);
				new_alt.calaculate_scores(current_seq,config);
				add_mod_seqs(new_alt);
				count++;
			}
		}
	}
}




// looks for holes btween two strong amino acids, tries other permutations over there
void MSB_candidate::treat_low_prob_holes(Config *config)
{
	int i;
	for (i=0; i<current_seq.seq_length-3; i++)
	{
		if (current_seq.aa_probs[i]>0.9 && current_seq.aa_probs[i+1]<0.6 &&
			current_seq.aa_probs[i+2]<0.6 && current_seq.aa_probs[i+3]>0.9 &&
			current_seq.seq[i+1] != X_SYM && current_seq.seq[i+2] != X_SYM)
		{
			MSB_alternates new_alt;
			MSB_seq new_seq;
			int rev_aa[]={current_seq.seq[i+2],current_seq.seq[i+1]};

			new_seq.clone_and_replace(current_seq,i+1,2,(int *)&rev_aa,2);
			new_seq.aa_probs[i+1] -= 0.03;
			if (new_seq.aa_probs[i+1]<0.03)
				new_seq.aa_probs[i+1]= 0.03;
			new_seq.aa_probs[i+2] -= 0.03;
			if (new_seq.aa_probs[i+2]<0.03)
				new_seq.aa_probs[i+2]= 0.03;

			new_alt.alt_seqs.push_back(new_seq);
			new_alt.alt_seqs.push_back(current_seq);
			new_alt.calaculate_scores(current_seq,config);

			add_mod_seqs(new_alt);
		}
	}
}


/**************************************************************
// adds a gap for low prob stretches
***************************************************************/
void MSB_candidate::replace_with_gap(Config *config)
{
	const float gap_prob = 0.45;
	const float min_prob_for_aa = 0.05;
	const vector<float> & aa2mass = config->get_aa2mass();
	int i;

	for (i=0; i<current_seq.seq_length-2; i++)
	{

		if (current_seq.aa_probs[i] > 0.67 || current_seq.seq[i] > 100)
			continue;

		float prob = 1.0;
		float mass = 0;
		int j;

		for (j=i; j<current_seq.seq_length; j++)
		{
			if (current_seq.aa_probs[j]>0.6 || current_seq.seq[j] > 100 || 
				current_seq.marked_aa[j])
			{
				j--;
				break;
			}


			float p = current_seq.aa_probs[j];
			if (p<0.03)
				p=0.03;

			if (j>i && pow(prob*p,1.0/(j+1-i))> gap_prob)
			{
				j--;
				break;
			}
		
			prob *= p;
			mass += aa2mass[current_seq.seq[j]];

			if (mass>400)
				break;
		}

	

		// remove last aa from gap if it is more than prob 0.55
		if (j == current_seq.seq_length)
			j--;

		while (j>i && current_seq.aa_probs[j]>0.55)
		{
			mass -= aa2mass[current_seq.seq[j]];
			

			float p = current_seq.aa_probs[j];
			if (p<0.03)
				p=0.03;

			prob /= p;
			j--;
		}

		int aa_length = j-i+1;
		if (aa_length<2)
			continue;

		// don't want a gap endig the sequence
		if (i+aa_length >= current_seq.seq_length -1)
			continue;

		prob = pow(prob,1.0/aa_length);

		if (prob<gap_prob)
		{
			int j;
			int arr_idx = (int)(2*mass+0.5);
			if (arr_idx>=1200)
				continue;

			int x_syms[]={X_SYM,X_SYM,X_SYM,X_SYM,X_SYM,X_SYM,X_SYM,X_SYM};
			MSB_alternates new_alt;
			for (j=7; j>=1; j--)
			{
				if (gap_aa_probs[arr_idx][j]>min_prob_for_aa)
				{
					
					MSB_seq new_seq;
					new_seq.clone_and_replace(current_seq,i,aa_length,(int *)(&x_syms),j);

					// check that new seq doesn't have bad properties like
					// terminating with XXXXXXXX, too many XXX
					if (new_seq.seq[new_seq.seq_length-1] == X_SYM)
						break;
					
					int num_X=0;
					int num_aa=0;
					int t;
					for (t=0; t<new_seq.seq_length; t++)
						if (new_seq.seq[t] == X_SYM)
						{
							num_X++;
						}
						else
							num_aa++;

					// don't allow XXX prefix - makes no sense
					if (new_seq.seq[0] == X_SYM)
						break;

					if (num_aa<=4 || num_X>num_aa || num_X>7)
						break;

					new_alt.alt_seqs.push_back(new_seq);
				}
			}

			if (new_alt.alt_seqs.size()>0)
			{
				new_alt.alt_seqs.push_back(current_seq);
				new_alt.calaculate_scores(current_seq,config);
				add_mod_seqs(new_alt);
			}
		}
		j+= aa_length;
	}
}


/**************************************************************
// if there are two amino acids, changes the order and also 
// fills in  XXX's.
***************************************************************/
void MSB_candidate::alter_prefix(Config *config)
{
	const float max_prob = 0.7;

	if (current_seq.seq[0] != B_SYM)
		return;

	if (current_seq.seq[1]>100 || current_seq.seq[2]>100)
		return;

	if (current_seq.marked_aa[1] || current_seq.marked_aa[2])
		return;

	float p = current_seq.aa_probs[1]*current_seq.aa_probs[2];
	if (p>0 && sqrt(p)>max_prob)
		return;

	int two_gap[] = {X_SYM,X_SYM};
	int rev_aa[] = {current_seq.seq[2],current_seq.seq[1]};
	MSB_alternates new_alt;
	MSB_seq gap_seq,rev_seq;
	gap_seq.clone_and_replace(current_seq,1,2,(int *)(&two_gap),2);
	rev_seq.clone_and_replace(current_seq,1,2,(int *)(&rev_aa),2);
	rev_seq.aa_probs[1]-=0.02;
	rev_seq.aa_probs[2]-=0.02;
	if (rev_seq.aa_probs[1]<0.05)
		rev_seq.aa_probs[1] = 0.05;
	if (rev_seq.aa_probs[2]<0.05)
		rev_seq.aa_probs[2] = 0.05;

	new_alt.alt_seqs.push_back(rev_seq);
	new_alt.alt_seqs.push_back(gap_seq);
	new_alt.alt_seqs.push_back(current_seq);
	new_alt.calaculate_scores(current_seq,config);

	add_mod_seqs(new_alt);
}

/******************************************************************
// removes the ends of the sequences if they have low probabilities
*******************************************************************/
void MSB_candidate::trim_ends(Config *config, int core_size)
{
	const float max_trim_prob = 0.6;
	const float first_two_trim = 0.7;
	const float after_good_prob = 0.3;
	const float non_tryptic_trim = 0.8;

	int i;

	// trim prefix

	for (i=0; i<current_seq.seq_length-core_size; i++)
	{
		if (i==0 && current_seq.seq[i] == B_SYM && current_seq.aa_probs[1] < first_two_trim)
			continue;

		if (i<2 && current_seq.aa_probs[i]<first_two_trim)
			continue;

		if (current_seq.aa_probs[i] < max_trim_prob)
			continue;

		if (i < current_seq.seq_length-core_size-1 &&
			current_seq.aa_probs[i+1] < after_good_prob)
			continue;
		if (current_seq.seq[i] == X_SYM)
			continue;

		break;
	}


	// we can trim..
	if ((current_seq.seq[0] != B_SYM && i>0) || i>2 )
	{
		MSB_alternates new_alt;
		MSB_seq trim_seq;

		trim_seq.clone_and_replace(current_seq,0,i,(int *)(NULL),0);
		new_alt.alt_seqs.push_back(trim_seq);

		new_alt.alt_seqs.push_back(current_seq);
		new_alt.calaculate_scores(current_seq,config);

		add_mod_seqs(new_alt);
	}

	// trim suffix
	for (i=current_seq.seq_length-1; i>0; i--)
	{
		if (i==current_seq.seq_length-1 && current_seq.seq[i] != Arg && 
			current_seq.seq[i] != Lys && current_seq.aa_probs[i] < non_tryptic_trim)
			continue;

		if (i>current_seq.seq_length-3 && current_seq.aa_probs[i] < first_two_trim)
			continue;

		if (current_seq.aa_probs[i] < max_trim_prob)
			continue;

		if (current_seq.seq[i] == X_SYM)
			continue;

		if (i <core_size)
			break;
	}

	// we can trim..
	if (i < current_seq.seq_length-1 && i>= core_size -1)
	{
		MSB_alternates new_alt;
		MSB_seq trim_seq;

		trim_seq.clone_and_replace(current_seq,i+1,current_seq.seq_length - i -1,(int *)(NULL),0);
		new_alt.alt_seqs.push_back(trim_seq);

		new_alt.alt_seqs.push_back(current_seq);
		new_alt.calaculate_scores(current_seq,config);

		add_mod_seqs(new_alt);
	}
}

